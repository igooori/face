from aiogram import Router,F
from aiogram.types import CallbackQuery
from keyboards.canel import get_canel

router = Router()

work_txt = """<b>⚙️ Как я анализирую твоё лицо?</b>

Когда ты присылаешь фото, я провожу многоуровневый анализ:

1️⃣ <b>Геометрия и пропорции</b>
Нейросеть находит 68+ ключевых точек лица и измеряет симметрию, золотое сечение, соотношение третей лица, ширину скул и челюсти.

2️⃣ <b>Гармония черт</b>
Оцениваю форму и расположение глаз, носа, губ, лба — насколько они создают приятную общую картину.

3️⃣ <b>Качество кожи и возраст</b>
Анализирую текстуру кожи, ровность тона, морщины, состояние — и определяю визуальный возраст.

В итоге я собираю все данные и выдаю тебе честную оценку от 0 до 100 + персональные рекомендации.

<i>Чем лучше фото (хороший свет, анфас, нейтральное выражение), тем точнее результат.</i>

📸 Готов? Присылай фото!"""


examples_text = """<b>📊 Пример анализа</b>

<b>Оценка привлекательности: 87/100</b> 🔥

<b>Разбор по параметрам:</b>
• Симметрия лица — 92%
• Пропорции и золотое сечение — 89%
• Гармония черт — 85%
• Состояние кожи — 78%
• Визуальный возраст — 24 (при реальном 27)

<b>Сильные стороны:</b>
✓ Выразительные глаза и правильная форма бровей
✓ Чёткая линия челюсти и высокие скулы
✓ Хорошая симметрия

<b>Что можно улучшить:</b>
→ Небольшая работа с кожей (акне + постакне)
→ Причёска с объёмом сверху визуально вытянет лицо
→ Угол съёмки чуть выше + лёгкая улыбка добавят +5-7 баллов

<i>Хочешь такой же подробный разбор своего лица?</i>

📸 Просто отправь своё фото!"""

@router.callback_query(F.data == 'works')
async def works_inline(callback: CallbackQuery):
    await callback.message.answer(text=work_txt,reply_markup=get_canel(),parse_mode='HTML')
    await callback.answer()
@router.callback_query(F.data == 'example')
async def example_inline(callback: CallbackQuery):
    await callback.message.answer(text=examples_text,reply_markup=get_canel(), parse_mode='HTML')